



const dropdownButton15 = document.getElementById('dropdownButton15');
const dropdownMenu15 = document.getElementById('dropdownMenu15');

// إضافة حدث النقر على الأيقونة
dropdownButton15.addEventListener('click', function() {
    // ابدل حالة العرض للقائمة المنسدلة
    if (dropdownMenu15.style.display === 'none') {
        dropdownMenu15.style.display = 'block';
    } else {
        dropdownMenu15.style.display = 'none';
    }
});



