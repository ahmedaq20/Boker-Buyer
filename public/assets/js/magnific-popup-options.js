// احضر العناصر من الواجهة الHTML
const dropdownButton2 = document.getElementById('dropdownButton2');
const dropdownMenu2 = document.getElementById('dropdownMenu2');

// إضافة حدث النقر على الأيقونة
dropdownButton2.addEventListener('click', function() {
    // ابدل حالة العرض للقائمة المنسدلة
    if (dropdownMenu2.style.display === 'none') {
        dropdownMenu2.style.display = 'block';
    } else {
        dropdownMenu2.style.display = 'none';
    }
});



